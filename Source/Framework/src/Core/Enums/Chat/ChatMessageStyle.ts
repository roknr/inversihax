/**
 * Type of chat message formatting style.
 */
export enum ChatMessageStyle {

    /**
     * The normal chat message style.
     */
    Normal = "normal",

    /**
     * Chat message is formatted in bold.
     */
    Bold = "bold",

    /**
     * Chat message is formatted in italic.
     */
    Italic = "italic",

    /**
     * Chat message is formatted in small.
     */
    Small = "small",

    /**
     * Chat message is formatted in small bold.
     */
    SmallBold = "small-bold",

    /**
     * Chat message is formatted in small italic.
     */
    SmallItalic = "small-italic",
}